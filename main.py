def set_zoom(scale):
    """Ekranı verilen ölçeğe göre ayarlar (Zoom In/Out)"""
    offset_x = 300
    offset_y = 300
    screen.setworldcoordinates(250 * scale + offset_x, 250 * scale + offset_y, -250 * scale + offset_x, -250 * scale + offset_y)

