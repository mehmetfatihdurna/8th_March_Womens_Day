def draw_filled_shape(coords, color):
    """
    Turtle ile verilen koordinatları birleştirerek şekil çizen ve içini verilen renkle boyayan fonksiyon.
    
    :param coords: [(x1, y1), (x2, y2), ..., (xn, yn)] şeklinde koordinat listesi
    :param color: Şeklin iç rengini belirten string (örn: "red", "#3498db")
    """
    turtle.penup()
    turtle.pencolor(color)
    turtle.goto(coords[0])  # İlk noktaya git
    turtle.pendown()

    turtle.fillcolor(color)  # Dolgu rengini ayarla
    turtle.begin_fill()  # Boyamayı başlat

    for x, y in coords:  # Tüm noktaları dolaş
        turtle.goto(x, y)

    turtle.goto(coords[0])  # Şekli kapatmak için başlangıç noktasına dön
    turtle.end_fill()  # Boyamayı tamamla



